public class Day5 {

  public static void main(String[] args) {
  
    // operasi aritmatika
    
    int angka1 = 6;
    int angka2 = 4;
     
     int hasil;
     
    // penjumlahan
      hasil = angka1 + angka2;
      System.out.println(angka1 + "+" + angka2 + "=" + hasil);
    
    
    // pengurangan
      hasil = angka1 - angka2;
      System.out.printf("%d - %d = %d /n",angka1,angka2,hasil);
      
     // perkalian
      hasil = angka1 * angka2;
      System.out.printf("%d * %d = %d /n",angka1,angka2,hasil);
      
      // pembagian
       hasil = angka1 / angka2;
       System.out.printf("%d / %d = %d /n",angka1,angka2,hasil);
      
      // modulus
       hasil = angka1 % angka2;
       System.out.printf("%d %% %d =%d /n",angka1,angka2,hasil);
         
    }
    
}
