package Day11;

public class Day11 {

  public static void main(String[] args) {
 
   // kita akan membuat operator logika
   
   boolean a = true ;
   boolean b = false ;
   boolean hasil;
   
   hasil = a && b;
   System.out.println("hasil dari a && b :" + hasil);
   
   hasil = a || b;
   System.out.println("hasil dari a || b :" + hasil);
   
   hasil = !a;
   System.out.println("hasil dari !b :" + hasil);
   
  }
}
